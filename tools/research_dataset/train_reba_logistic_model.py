@dataclass(frozen=True)
class Point:
    x: float
    y: float


@dataclass(frozen=True)
class RebaPseudoLabel:
    score: int
    risk_level: str
    target_probability: float
    trunk_score: int
    neck_score: int
    leg_score: int
    upper_arm_score: int
    lower_arm_score: int
    wrist_score: int
    load_score: int
    coupling_score: int
    activity_score: int
    pose_quality: float


