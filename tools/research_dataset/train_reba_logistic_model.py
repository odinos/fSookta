def numeric(row: dict[str, str], key: str, default: float = 0.0) -> float:
    try:
        value = row.get(key, "")
        return float(value) if value != "" else default
    except ValueError:
        return default


def point(row: dict[str, str], landmark: str, min_score: float = 0.3) -> Point | None:
    score = numeric(row, f"{landmark}_score")
    if score < min_score:
        return None
    return Point(numeric(row, f"{landmark}_x"), numeric(row, f"{landmark}_y"))


def midpoint(points: list[Point | None]) -> Point | None:
    visible = [p for p in points if p is not None]
    if not visible:
        return None
    return Point(
        sum(p.x for p in visible) / len(visible),
        sum(p.y for p in visible) / len(visible),
    )


def vertical_angle(p1: Point, p2: Point) -> float:
    dx = abs(p2.x - p1.x)
    dy = abs(p1.y - p2.y)
    return math.degrees(math.atan2(dx, dy))


def horizontal_angle(p1: Point, p2: Point) -> float:
    dx = abs(p2.x - p1.x)
    dy = abs(p2.y - p1.y)
    return math.degrees(math.atan2(dy, dx))


def distance(p1: Point, p2: Point) -> float:
    return math.hypot(p2.x - p1.x, p2.y - p1.y)


def three_point_angle(p1: Point, p2: Point, p3: Point) -> float:
    a1 = math.atan2(p1.y - p2.y, p1.x - p2.x)
    a2 = math.atan2(p3.y - p2.y, p3.x - p2.x)
    angle = math.degrees(a1 - a2)
    if angle < 0:
        angle += 360
    if angle > 180:
        angle = 360 - angle
    return angle


def angle_or_zero(points: tuple[Point | None, Point | None]) -> float:
    first, second = points
    if first is None or second is None:
        return 0.0
    return vertical_angle(first, second)


def joint_angle_or_zero(points: tuple[Point | None, Point | None, Point | None]) -> float:
    first, second, third = points
    if first is None or second is None or third is None:
        return 0.0
    return three_point_angle(first, second, third)


def lower_arm_deviation(angle: float) -> float:
    if angle <= 0:
        return 0.0
    if 60 <= angle <= 100:
        return 0.0
    if angle < 60:
        return 60 - angle
    return angle - 100


def side_triplet(row: dict[str, str], names: tuple[str, str, str]) -> tuple[Point, Point, Point] | None:
    right = tuple(point(row, f"right{name}") for name in names)
    if all(right):
        return right  # type: ignore[return-value]
    left = tuple(point(row, f"left{name}") for name in names)
    if all(left):
        return left  # type: ignore[return-value]
    return None


def posture_score_a(trunk: int, neck: int, legs: int) -> int:
    # The worksheet uses Table A after neck, trunk, and leg scoring. This
    # compact implementation keeps the same directionality and score range for
    # deterministic pseudo-label generation from images.
    score = trunk + (1 if neck >= 2 else 0) + (1 if legs >= 2 else 0)
    return min(max(score, 1), 9)


def posture_score_b(upper: int, lower: int, wrist: int) -> int:
    # Table B combines upper arm, lower arm, and wrist posture before coupling.
    score = upper + (1 if lower >= 2 else 0) + (1 if wrist >= 2 else 0)
    return min(max(score, 1), 9)


def table_c_score(score_a: int, score_b: int) -> int:
    # REBA Table C increases the final posture score as either side of the body
    # becomes more severe. This monotonic approximation is stable for training
    # and mirrors the app's current REBA calculator.
    score = max(score_a, score_b)
    if min(score_a, score_b) >= 6:
        score += 1
    return min(max(score, 1), 12)


def risk_level_for_reba(score: int) -> str:
    if score <= 1:
        return "low"
    if score <= 3:
        return "low"
    if score <= 7:
        return "medium"
    if score <= 10:
        return "high"
    return "veryHigh"


def risk_level_for_reba_float(score: float) -> str:
    return risk_level_for_reba(int(round(score)))


def risk_level_from_thai(value: str) -> str:
    normalized = value.strip().lower()
    if normalized in {"ต่ำ", "low"}:
        return "low"
    if normalized in {"ปานกลาง", "medium"}:
        return "medium"
    if normalized in {"สูง", "high"}:
        return "high"
    if normalized in {"สูงมาก", "veryhigh", "very_high", "critical"}:
        return "veryHigh"
    return "medium"


def risk_level_for_iso_total(score: float) -> str:
    if score <= 8:
        return "low"
    if score <= 13:
        return "medium"
    if score <= 18:
        return "high"
    return "veryHigh"


def activity_defaults(activity: str) -> tuple[int, int, int]:
    normalized = activity.strip().lower()
    if normalized == "on_farm_transport":
        return 2, 2, 1
    if normalized == "harvesting":
        return 1, 1, 1
    if normalized == "pruning":
        return 0, 1, 1
    return 0, 1, 1


def derive_reba_label(row: dict[str, str]) -> RebaPseudoLabel:
    shoulders = midpoint([point(row, "leftShoulder"), point(row, "rightShoulder")])
    hips = midpoint([point(row, "leftHip"), point(row, "rightHip")])
    head = midpoint([point(row, "nose"), point(row, "leftEar"), point(row, "rightEar")])

    trunk_score = 2
    if shoulders and hips:
        trunk_angle = vertical_angle(hips, shoulders)
        if trunk_angle <= 5:
            trunk_score = 1
        elif trunk_angle <= 20:
            trunk_score = 2
        elif trunk_angle <= 60:
            trunk_score = 3
        else:
            trunk_score = 4

    neck_score = 1
    if shoulders and head:
        neck_score = 1 if vertical_angle(shoulders, head) <= 20 else 2

    leg_score = 1
    leg = side_triplet(row, ("Hip", "Knee", "Ankle"))
    if leg:
        hip, knee, ankle = leg
        leg_score = 2 if three_point_angle(hip, knee, ankle) < 150 else 1

    upper_arm_score = 2
    arm = side_triplet(row, ("Shoulder", "Elbow", "Wrist"))
    if arm:
        shoulder, elbow, wrist = arm
        upper_angle = vertical_angle(shoulder, elbow)
        if upper_angle <= 20:
            upper_arm_score = 1
        elif upper_angle <= 45:
            upper_arm_score = 2
        elif upper_angle <= 90:
            upper_arm_score = 3
        else:
            upper_arm_score = 4
        lower_angle = three_point_angle(shoulder, elbow, wrist)
        lower_arm_score = 1 if 60 <= lower_angle <= 100 else 2
        wrist_score = 1
    else:
        lower_arm_score = 2
        wrist_score = 1

    load_score, coupling_score, activity_score = activity_defaults(row.get("activity", ""))
    score_a = posture_score_a(trunk_score, neck_score, leg_score) + load_score
    score_b = posture_score_b(upper_arm_score, lower_arm_score, wrist_score) + coupling_score
    final_score = min(table_c_score(score_a, score_b) + activity_score, 15)
    risk_level = risk_level_for_reba(final_score)

    # Map the REBA score to a soft target. The app still applies thresholds
    # 4/8/11 through probability boundaries when rendering low/medium/high.
    target_probability = max(0.0, min(1.0, (final_score - 1) / 11))
    return RebaPseudoLabel(
        score=final_score,
        risk_level=risk_level,
        target_probability=target_probability,
        trunk_score=trunk_score,
        neck_score=neck_score,
        leg_score=leg_score,
        upper_arm_score=upper_arm_score,
        lower_arm_score=lower_arm_score,
        wrist_score=wrist_score,
        load_score=load_score,
        coupling_score=coupling_score,
        activity_score=activity_score,
        pose_quality=numeric(row, "pose_score"),
    )


def iso_label_from_rows(
    *,
    activity: str,
    session_id: str,
    iso_rows: list[dict[str, str]],
) -> Iso11228Label | None:
    exact = [
        row
        for row in iso_rows
        if row.get("activity") == activity
        and str(row.get("session_id", "")).strip() == session_id
    ]
    if exact:
        return _mean_iso_label(exact, "iso11228_exact", exact[0].get("session_id", ""))

    prefix = f"{session_id}."
    prefix_matches = [
        row
        for row in iso_rows
        if row.get("activity") == activity
        and str(row.get("session_id", "")).strip().startswith(prefix)
    ]
    if prefix_matches:
        return _mean_iso_label(
            prefix_matches,
            "iso11228_prefix_mean",
            "+".join(row.get("session_id", "") for row in prefix_matches),
        )

    activity_matches = [row for row in iso_rows if row.get("activity") == activity]
    if activity_matches:
        return _mean_iso_label(
            activity_matches,
            "iso11228_activity_mean",
            "+".join(row.get("session_id", "") for row in activity_matches),
        )
    return None


def _mean_iso_label(
    rows: list[dict[str, str]],
    match_type: str,
    matched_session_id: str,
) -> Iso11228Label:
    total = sum(numeric(row, "iso11228_total_score") for row in rows) / len(rows)
    risk_counts = Counter(
        risk_level_from_thai(row.get("risk_level_th", "")) for row in rows
    )
    risk = risk_counts.most_common(1)[0][0] if risk_counts else risk_level_for_iso_total(total)
    source = rows[0].get("label_source") or "research_team_iso11228_workbook"
    return Iso11228Label(
        total_score=total,
        risk_level=risk,
        label_source=source,
        matched_session_id=matched_session_id,
        match_type=match_type,
    )


def document_guided_iso_label(
    row: dict[str, str],
    pseudo: RebaPseudoLabel,
) -> Iso11228Label | None:
    activity = row.get("activity", "")
    if activity not in {"pruning", "harvesting", "transplanting", "pesticide_spraying"}:
        return None

    posture = max(pseudo.trunk_score, pseudo.neck_score, pseudo.leg_score)
    arms = max(pseudo.upper_arm_score, pseudo.wrist_score)
    force = 1 + min(2, pseudo.load_score)
    frequency = 2 + min(1, pseudo.activity_score)
    reach = 3 if pseudo.upper_arm_score >= 3 else 2 if arms >= 2 else 1
    environment = 1
    total = posture + arms + force + frequency + reach + environment

    if total < 10 and pseudo.activity_score == 0 and arms < 3:
        return None

    return Iso11228Label(
        total_score=float(total),
        risk_level=risk_level_for_iso_total(float(total)),
        label_source="iso11228_3_document_guided_pseudo_label",
        matched_session_id="",
        match_type="document_guided_iso11228_3",
    )


def build_training_label(
    row: dict[str, str],
    pseudo: RebaPseudoLabel,
    *,
    exact_expert_labels: dict[tuple[str, str], ExpertRebaLabel],
    expert_labels: list[dict[str, str]],
    iso_labels: list[dict[str, str]],
) -> TrainingLabel:
    activity = row.get("activity", "")
    session_id = str(row.get("session_id", "")).strip()
    iso = iso_label_from_rows(
        activity=activity,
        session_id=session_id,
        iso_rows=iso_labels,
    ) or document_guided_iso_label(row, pseudo)
    exact = exact_expert_labels.get((activity, session_id))
    if exact is not None:
        score = max(exact.score, iso.reba_equivalent_score) if iso else exact.score
        probability = max(exact.target_probability, iso.target_probability) if iso else exact.target_probability
        risk = risk_level_for_reba_float(score)
        return TrainingLabel(
            score=score,
            risk_level=risk,
            target_probability=probability,
            label_source=exact.label_source + ("+" + iso.label_source if iso else ""),
            match_type="expert_exact" + ("+" + iso.match_type if iso else ""),
            matched_session_id=exact.matched_session_id,
            pseudo=pseudo,
            reba_score=exact.score,
            reba_risk_level=exact.risk_level,
            iso_total_score=iso.total_score if iso else None,
            iso_risk_level=iso.risk_level if iso else None,
            iso_label_source=iso.label_source if iso else "",
            iso_match_type=iso.match_type if iso else "",
            iso_matched_session_id=iso.matched_session_id if iso else "",
        )

    prefix_matches = []
    prefix = f"{session_id}."
    for label in expert_labels:
        if label.get("activity") != activity:
            continue
        candidate_session = str(label.get("session_id", "")).strip()
        if candidate_session.startswith(prefix):
            prefix_matches.append(label)
    if prefix_matches:
        score = sum(numeric(label, "reba_score") for label in prefix_matches) / len(prefix_matches)
        matched_sessions = "+".join(label["session_id"] for label in prefix_matches)
        reba_risk = risk_level_for_reba_float(score)
        combined_score = max(score, iso.reba_equivalent_score) if iso else score
        probability = max(0.0, min(1.0, (score - 1) / 11))
        if iso:
            probability = max(probability, iso.target_probability)
        return TrainingLabel(
            score=combined_score,
            risk_level=risk_level_for_reba_float(combined_score),
            target_probability=probability,
            label_source="research_team_reba2_prefix_mean"
            + ("+" + iso.label_source if iso else ""),
            match_type="expert_prefix_mean" + ("+" + iso.match_type if iso else ""),
            matched_session_id=matched_sessions,
            pseudo=pseudo,
            reba_score=score,
            reba_risk_level=reba_risk,
            iso_total_score=iso.total_score if iso else None,
            iso_risk_level=iso.risk_level if iso else None,
            iso_label_source=iso.label_source if iso else "",
            iso_match_type=iso.match_type if iso else "",
            iso_matched_session_id=iso.matched_session_id if iso else "",
        )

    score = max(float(pseudo.score), iso.reba_equivalent_score) if iso else float(pseudo.score)
    probability = max(pseudo.target_probability, iso.target_probability) if iso else pseudo.target_probability
    return TrainingLabel(
        score=score,
        risk_level=risk_level_for_reba_float(score),
        target_probability=probability,
        label_source="reba_worksheet_pseudo_label"
        + ("+" + iso.label_source if iso else ""),
        match_type="pseudo_fallback" + ("+" + iso.match_type if iso else ""),
        matched_session_id="",
        pseudo=pseudo,
        reba_score=float(pseudo.score),
        reba_risk_level=pseudo.risk_level,
        iso_total_score=iso.total_score if iso else None,
        iso_risk_level=iso.risk_level if iso else None,
        iso_label_source=iso.label_source if iso else "",
        iso_match_type=iso.match_type if iso else "",
        iso_matched_session_id=iso.matched_session_id if iso else "",
    )


def engineered_features(row: dict[str, str]) -> list[float]:
    left_shoulder = point(row, "leftShoulder")
    right_shoulder = point(row, "rightShoulder")
    left_elbow = point(row, "leftElbow")
    right_elbow = point(row, "rightElbow")
    left_wrist = point(row, "leftWrist")
    right_wrist = point(row, "rightWrist")
    left_hip = point(row, "leftHip")
    right_hip = point(row, "rightHip")
    left_knee = point(row, "leftKnee")
    right_knee = point(row, "rightKnee")
    left_ankle = point(row, "leftAnkle")
    right_ankle = point(row, "rightAnkle")
    shoulders = midpoint([left_shoulder, right_shoulder])
    hips = midpoint([left_hip, right_hip])
    head = midpoint([point(row, "nose"), point(row, "leftEar"), point(row, "rightEar")])

    trunk_angle = angle_or_zero((hips, shoulders))
    neck_angle = angle_or_zero((shoulders, head))
    left_upper_arm_angle = angle_or_zero((left_shoulder, left_elbow))
    right_upper_arm_angle = angle_or_zero((right_shoulder, right_elbow))
    worst_upper_arm_angle = max(left_upper_arm_angle, right_upper_arm_angle)
    left_elbow_angle = joint_angle_or_zero((left_shoulder, left_elbow, left_wrist))
    right_elbow_angle = joint_angle_or_zero((right_shoulder, right_elbow, right_wrist))
    worst_lower_arm_deviation = max(
        lower_arm_deviation(left_elbow_angle),
        lower_arm_deviation(right_elbow_angle),
    )
    left_knee_angle = joint_angle_or_zero((left_hip, left_knee, left_ankle))
    right_knee_angle = joint_angle_or_zero((right_hip, right_knee, right_ankle))
    worst_knee_flexion = max(0.0, 180 - min(left_knee_angle or 180, right_knee_angle or 180))
    shoulder_slope = (
        horizontal_angle(left_shoulder, right_shoulder)
        if left_shoulder is not None and right_shoulder is not None
        else 0.0
    )
    hip_slope = (
        horizontal_angle(left_hip, right_hip)
        if left_hip is not None and right_hip is not None
        else 0.0
    )
    shoulder_width = (
        distance(left_shoulder, right_shoulder)
        if left_shoulder is not None and right_shoulder is not None
        else 0.0
    )
    hip_width = (
        distance(left_hip, right_hip)
        if left_hip is not None and right_hip is not None
        else 0.0
    )
    upper_body_lean = abs(shoulders.x - hips.x) if shoulders is not None and hips is not None else 0.0

    scores = [numeric(row, column) for column in FEATURE_COLUMNS if column.endswith("_score")]
    visible = [score for score in scores if score >= 0.3]
    upper_scores = [
        numeric(row, f"{landmark}_score")
        for landmark in [
            "nose",
            "leftEar",
            "rightEar",
            "leftShoulder",
            "rightShoulder",
            "leftElbow",
            "rightElbow",
            "leftWrist",
            "rightWrist",
        ]
    ]
    lower_scores = [
        numeric(row, f"{landmark}_score")
        for landmark in [
            "leftHip",
            "rightHip",
            "leftKnee",
            "rightKnee",
            "leftAnkle",
            "rightAnkle",
        ]
    ]
    return [
        trunk_angle,
        neck_angle,
        left_upper_arm_angle,
        right_upper_arm_angle,
        worst_upper_arm_angle,
        left_elbow_angle,
        right_elbow_angle,
        worst_lower_arm_deviation,
        left_knee_angle,
        right_knee_angle,
        worst_knee_flexion,
        shoulder_slope,
        hip_slope,
        shoulder_width,
        hip_width,
        upper_body_lean,
        len(visible) / len(scores),
        sum(scores) / len(scores),
        sum(1 for score in lower_scores if score >= 0.3) / len(lower_scores),
        sum(1 for score in upper_scores if score >= 0.3) / len(upper_scores),
    ]


def feature_vector(row: dict[str, str]) -> list[float]:
    # Keep the canonical MoveNet 51 values, then append REBA-oriented geometry
    # features. The Flutter app performs the same expansion at inference time.
    return [numeric(row, column) for column in FEATURE_COLUMNS] + engineered_features(row)


def feature_names() -> list[str]:
    return [*FEATURE_COLUMNS, *ENGINEERED_FEATURE_NAMES]


