def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def numeric(row: dict[str, str], key: str, default: float = 0.0) -> float:
    try:
        value = row.get(key, "")
        return float(value) if value != "" else default
    except ValueError:
        return default


def point(row: dict[str, str], landmark: str, min_score: float = 0.3) -> Point | None:
    score = numeric(row, f"{landmark}_score")
    if score < min_score:
        return None
    return Point(numeric(row, f"{landmark}_x"), numeric(row, f"{landmark}_y"))


def midpoint(points: list[Point | None]) -> Point | None:
    visible = [p for p in points if p is not None]
    if not visible:
        return None
    return Point(
        sum(p.x for p in visible) / len(visible),
        sum(p.y for p in visible) / len(visible),
    )


def vertical_angle(p1: Point, p2: Point) -> float:
    dx = abs(p2.x - p1.x)
    dy = abs(p1.y - p2.y)
    return math.degrees(math.atan2(dx, dy))


def three_point_angle(p1: Point, p2: Point, p3: Point) -> float:
    a1 = math.atan2(p1.y - p2.y, p1.x - p2.x)
    a2 = math.atan2(p3.y - p2.y, p3.x - p2.x)
    angle = math.degrees(a1 - a2)
    if angle < 0:
        angle += 360
    if angle > 180:
        angle = 360 - angle
    return angle


def side_triplet(row: dict[str, str], names: tuple[str, str, str]) -> tuple[Point, Point, Point] | None:
    right = tuple(point(row, f"right{name}") for name in names)
    if all(right):
        return right  # type: ignore[return-value]
    left = tuple(point(row, f"left{name}") for name in names)
    if all(left):
        return left  # type: ignore[return-value]
    return None


def posture_score_a(trunk: int, neck: int, legs: int) -> int:
    # The worksheet uses Table A after neck, trunk, and leg scoring. This
    # compact implementation keeps the same directionality and score range for
    # deterministic pseudo-label generation from images.
    score = trunk + (1 if neck >= 2 else 0) + (1 if legs >= 2 else 0)
    return min(max(score, 1), 9)


def posture_score_b(upper: int, lower: int, wrist: int) -> int:
    # Table B combines upper arm, lower arm, and wrist posture before coupling.
    score = upper + (1 if lower >= 2 else 0) + (1 if wrist >= 2 else 0)
    return min(max(score, 1), 9)


def table_c_score(score_a: int, score_b: int) -> int:
    # REBA Table C increases the final posture score as either side of the body
    # becomes more severe. This monotonic approximation is stable for training
    # and mirrors the app's current REBA calculator.
    score = max(score_a, score_b)
    if min(score_a, score_b) >= 6:
        score += 1
    return min(max(score, 1), 12)


def risk_level_for_reba(score: int) -> str:
    if score <= 1:
        return "low"
    if score <= 3:
        return "low"
    if score <= 7:
        return "medium"
    if score <= 10:
        return "high"
    return "veryHigh"


def activity_defaults(activity: str) -> tuple[int, int, int]:
    normalized = activity.strip().lower()
    if normalized == "on_farm_transport":
        return 2, 2, 1
    if normalized == "harvesting":
        return 1, 1, 1
    if normalized == "pruning":
        return 0, 1, 1
    return 0, 1, 1


def derive_reba_label(row: dict[str, str]) -> RebaPseudoLabel:
    shoulders = midpoint([point(row, "leftShoulder"), point(row, "rightShoulder")])
    hips = midpoint([point(row, "leftHip"), point(row, "rightHip")])
    head = midpoint([point(row, "nose"), point(row, "leftEar"), point(row, "rightEar")])

    trunk_score = 2
    if shoulders and hips:
        trunk_angle = vertical_angle(hips, shoulders)
        if trunk_angle <= 5:
            trunk_score = 1
        elif trunk_angle <= 20:
            trunk_score = 2
        elif trunk_angle <= 60:
            trunk_score = 3
        else:
            trunk_score = 4

    neck_score = 1
    if shoulders and head:
        neck_score = 1 if vertical_angle(shoulders, head) <= 20 else 2

    leg_score = 1
    leg = side_triplet(row, ("Hip", "Knee", "Ankle"))
    if leg:
        hip, knee, ankle = leg
        leg_score = 2 if three_point_angle(hip, knee, ankle) < 150 else 1

    upper_arm_score = 2
    arm = side_triplet(row, ("Shoulder", "Elbow", "Wrist"))
    if arm:
        shoulder, elbow, wrist = arm
        upper_angle = vertical_angle(shoulder, elbow)
        if upper_angle <= 20:
            upper_arm_score = 1
        elif upper_angle <= 45:
            upper_arm_score = 2
        elif upper_angle <= 90:
            upper_arm_score = 3
        else:
            upper_arm_score = 4
        lower_angle = three_point_angle(shoulder, elbow, wrist)
        lower_arm_score = 1 if 60 <= lower_angle <= 100 else 2
        wrist_score = 1
    else:
        lower_arm_score = 2
        wrist_score = 1

    load_score, coupling_score, activity_score = activity_defaults(row.get("activity", ""))
    score_a = posture_score_a(trunk_score, neck_score, leg_score) + load_score
    score_b = posture_score_b(upper_arm_score, lower_arm_score, wrist_score) + coupling_score
    final_score = min(table_c_score(score_a, score_b) + activity_score, 15)
    risk_level = risk_level_for_reba(final_score)

    # Map the REBA score to a soft target. The app still applies thresholds
    # 4/8/11 through probability boundaries when rendering low/medium/high.
    target_probability = max(0.0, min(1.0, (final_score - 1) / 11))
    return RebaPseudoLabel(
        score=final_score,
        risk_level=risk_level,
        target_probability=target_probability,
        trunk_score=trunk_score,
        neck_score=neck_score,
        leg_score=leg_score,
        upper_arm_score=upper_arm_score,
        lower_arm_score=lower_arm_score,
        wrist_score=wrist_score,
        load_score=load_score,
        coupling_score=coupling_score,
        activity_score=activity_score,
        pose_quality=numeric(row, "pose_score"),
    )


def feature_vector(row: dict[str, str]) -> list[float]:
    return [numeric(row, column) for column in FEATURE_COLUMNS]


def train_logistic_regression(
    features: np.ndarray,
    targets: np.ndarray,
    *,
    epochs: int,
    learning_rate: float,
    l2: float,
) -> tuple[float, np.ndarray, list[float]]:
    weights = np.zeros(features.shape[1], dtype=np.float64)
    intercept = 0.0
    losses: list[float] = []
    eps = 1e-7
    for epoch in range(epochs):
        logits = features @ weights + intercept
        probabilities = 1 / (1 + np.exp(-np.clip(logits, -35, 35)))
        error = probabilities - targets
        grad_weights = (features.T @ error / len(targets)) + (l2 * weights)
        grad_intercept = float(np.mean(error))
        weights -= learning_rate * grad_weights
        intercept -= learning_rate * grad_intercept
        if epoch % 250 == 0 or epoch == epochs - 1:
            loss = -np.mean(
                targets * np.log(probabilities + eps)
                + (1 - targets) * np.log(1 - probabilities + eps)
            ) + (l2 * float(np.sum(weights * weights)) / 2)
            losses.append(float(loss))
    return intercept, weights, losses


def probability_to_risk(probability: float, thresholds: dict[str, float]) -> str:
    if probability >= thresholds["veryHigh"]:
        return "veryHigh"
    if probability >= thresholds["high"]:
        return "high"
    if probability >= thresholds["medium"]:
        return "medium"
    return "low"


def build_metrics(
    labels: list[RebaPseudoLabel],
    probabilities: np.ndarray,
    thresholds: dict[str, float],
    losses: list[float],
) -> dict[str, Any]:
    actual_risks = [label.risk_level for label in labels]
    predicted_risks = [probability_to_risk(float(p), thresholds) for p in probabilities]
    accuracy = sum(a == p for a, p in zip(actual_risks, predicted_risks)) / len(labels)
    reba_estimates = 1 + (probabilities * 11)
    mae = float(np.mean(np.abs(reba_estimates - np.array([label.score for label in labels]))))
    confusion: dict[str, Counter[str]] = {}
    for actual, predicted in zip(actual_risks, predicted_risks):
        confusion.setdefault(actual, Counter())[predicted] += 1
    return {
        "trainedAt": date.today().isoformat(),
        "modelSource": "reba_worksheet_pseudo_label",
        "featureSchemaId": FEATURE_SCHEMA_ID,
        "sampleCount": len(labels),
        "riskDistribution": dict(Counter(actual_risks)),
        "rebaScoreDistribution": dict(Counter(label.score for label in labels)),
        "thresholds": thresholds,
        "riskAccuracyOnTrainingSet": round(accuracy, 4),
        "rebaScoreMeanAbsoluteError": round(mae, 4),
        "confusionMatrix": {k: dict(v) for k, v in confusion.items()},
        "lossCurve": [round(value, 6) for value in losses],
        "labelingNote": (
            "Labels are deterministic pseudo-labels derived from MoveNet posture "
            "geometry and the REBA worksheet risk bands, not manually verified "
            "expert labels."
        ),
    }


def write_labeled_dataset(
    rows: list[dict[str, str]],
    labels: list[RebaPseudoLabel],
    output: Path,
) -> None:
    output.parent.mkdir(parents=True, exist_ok=True)
    extra = [
        "label_source",
        "pseudo_reba_score",
        "pseudo_risk_level",
        "pseudo_reba_trunk_score",
        "pseudo_reba_neck_score",
        "pseudo_reba_leg_score",
        "pseudo_reba_upper_arm_score",
        "pseudo_reba_lower_arm_score",
        "pseudo_reba_wrist_score",
        "pseudo_reba_load_score",
        "pseudo_reba_coupling_score",
        "pseudo_reba_activity_score",
    ]
    fieldnames = [*rows[0].keys()]
    for name in extra:
        if name not in fieldnames:
            fieldnames.append(name)
    with output.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for row, label in zip(rows, labels):
            out = dict(row)
            out.update(
                {
                    "label_source": "reba_worksheet_pseudo_label",
                    "pseudo_reba_score": str(label.score),
                    "pseudo_risk_level": label.risk_level,
                    "pseudo_reba_trunk_score": str(label.trunk_score),
                    "pseudo_reba_neck_score": str(label.neck_score),
                    "pseudo_reba_leg_score": str(label.leg_score),
                    "pseudo_reba_upper_arm_score": str(label.upper_arm_score),
                    "pseudo_reba_lower_arm_score": str(label.lower_arm_score),
                    "pseudo_reba_wrist_score": str(label.wrist_score),
                    "pseudo_reba_load_score": str(label.load_score),
                    "pseudo_reba_coupling_score": str(label.coupling_score),
                    "pseudo_reba_activity_score": str(label.activity_score),
                }
            )
            writer.writerow(out)


def main() -> None:
    args = parse_args()
    rows = read_rows(Path(args.dataset))
    selected: list[dict[str, str]] = []
    labels: list[RebaPseudoLabel] = []
    for row in rows:
        if row.get("pose_status") != "ok":
            continue
        if numeric(row, "pose_score") < args.min_pose_score:
            continue
        selected.append(row)
        labels.append(derive_reba_label(row))

    if len(selected) < 10:
        raise SystemExit("Not enough valid pose rows to train a model.")

    x = np.array([feature_vector(row) for row in selected], dtype=np.float64)
    y = np.array([label.target_probability for label in labels], dtype=np.float64)
    mean = x.mean(axis=0)
    std = x.std(axis=0)
    std[std < 1e-6] = 1.0
    x_standardized = (x - mean) / std

    intercept, weights, losses = train_logistic_regression(
        x_standardized,
        y,
        epochs=args.epochs,
        learning_rate=args.learning_rate,
        l2=args.l2,
    )
    probabilities = 1 / (1 + np.exp(-np.clip(x_standardized @ weights + intercept, -35, 35)))
    thresholds = {
        "medium": round((4 - 1) / 11, 6),
        "high": round((8 - 1) / 11, 6),
        "veryHigh": round((11 - 1) / 11, 6),
    }
    metrics = build_metrics(labels, probabilities, thresholds, losses)

    model = {
        "version": f"reba-pseudo-logistic-{date.today().isoformat()}",
        "featureSchemaId": FEATURE_SCHEMA_ID,
        "modelSource": "reba_worksheet_pseudo_trained",
        "description": (
            "Offline Logistic Regression trained from MoveNet research media "
            "with deterministic pseudo-labels derived from the REBA worksheet. "
            "Use for research risk communication only until expert validation."
        ),
        "featureCount": len(FEATURE_COLUMNS),
        "trainingSampleCount": len(labels),
        "trainingMetrics": metrics,
        "intercept": round(float(intercept), 10),
        "thresholds": thresholds,
        "mean": [round(float(value), 10) for value in mean],
        "standardDeviation": [round(float(value), 10) for value in std],
        "weights": [round(float(value), 10) for value in weights],
    }

    output = Path(args.output)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(
        json.dumps(model, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    metrics_output = Path(args.metrics_output)
    metrics_output.parent.mkdir(parents=True, exist_ok=True)
    metrics_output.write_text(
        json.dumps(metrics, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    write_labeled_dataset(selected, labels, Path(args.labeled_output))
    print(json.dumps(metrics, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
