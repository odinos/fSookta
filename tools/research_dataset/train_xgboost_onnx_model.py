#!/usr/bin/env python3
"""Train the research XGBoost model and export it as ONNX.

The Flutter app's ONNX predictor receives the canonical 51-value MoveNet vector,
so this model intentionally trains on raw MoveNet joint features. The richer
71-feature Logistic Regression artifact is retained only as a legacy research
artifact. Current app scoring separates it from REBA/ISO and uses Logistic
Regression for 7-transaction daily injury follow-up prediction instead.
"""

from __future__ import annotations

import argparse
import csv
import json
from collections import Counter
from datetime import date
from pathlib import Path
from typing import Any

import numpy as np
from onnxmltools import convert_xgboost
from onnxmltools.convert.common.data_types import FloatTensorType
from sklearn.metrics import mean_absolute_error
from sklearn.model_selection import GroupShuffleSplit
from xgboost import XGBRegressor

from tools.research_dataset.train_reba_logistic_model import (
    FEATURE_COLUMNS,
    FEATURE_SCHEMA_ID,
)


THRESHOLDS = {
    "medium": round((4 - 1) / 11, 6),
    "high": round((8 - 1) / 11, 6),
    "veryHigh": round((11 - 1) / 11, 6),
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Train fSookta research XGBoost and export ONNX.",
    )
    parser.add_argument(
        "--dataset",
        default="data/research/extracted/reba_labeled_pose_dataset.csv",
        help="Labeled dataset generated by train_reba_logistic_model.py.",
    )
    parser.add_argument(
        "--output",
        default="assets/models/xgboost_model.onnx",
        help="Output ONNX model asset.",
    )
    parser.add_argument(
        "--metadata-output",
        default="assets/models/xgboost_model_metadata.json",
        help="Auditable metadata for the exported ONNX artifact.",
    )
    parser.add_argument(
        "--metrics-output",
        default="data/research/extracted/xgboost_onnx_metrics.json",
        help="Research metrics output. This path is ignored by git.",
    )
    parser.add_argument("--test-size", type=float, default=0.22)
    parser.add_argument("--random-state", type=int, default=42)
    return parser.parse_args()


def read_rows(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def numeric(row: dict[str, str], key: str) -> float:
    value = row.get(key, "")
    if value in ("", None):
        return 0.0
    try:
        return float(value)
    except ValueError:
        return 0.0


def target_probability(row: dict[str, str]) -> float:
    score = numeric(row, "training_reba_score")
    if score <= 0:
        score = numeric(row, "pseudo_reba_score")
    return max(0.0, min(1.0, (score - 1) / 11))


def probability_to_risk(probability: float) -> str:
    if probability >= THRESHOLDS["veryHigh"]:
        return "veryHigh"
    if probability >= THRESHOLDS["high"]:
        return "high"
    if probability >= THRESHOLDS["medium"]:
        return "medium"
    return "low"


def risk_accuracy(actual: list[str], probabilities: np.ndarray) -> float:
    predicted = [probability_to_risk(float(value)) for value in probabilities]
    return sum(a == p for a, p in zip(actual, predicted)) / len(actual)


def risk_confusion(actual: list[str], probabilities: np.ndarray) -> dict[str, dict[str, int]]:
    confusion: dict[str, Counter[str]] = {}
    for actual_risk, probability in zip(actual, probabilities):
        predicted = probability_to_risk(float(probability))
        confusion.setdefault(actual_risk, Counter())[predicted] += 1
    return {key: dict(value) for key, value in confusion.items()}


def evaluate(
    *,
    model: XGBRegressor,
    x: np.ndarray,
    y: np.ndarray,
    risks: list[str],
) -> dict[str, Any]:
    prediction = np.clip(model.predict(x), 0.0, 1.0)
    score_estimate = 1 + (prediction * 11)
    actual_score = 1 + (y * 11)
    return {
        "sampleCount": int(len(y)),
        "probabilityMeanAbsoluteError": round(float(mean_absolute_error(y, prediction)), 4),
        "combinedRebaEquivalentScoreMae": round(
            float(mean_absolute_error(actual_score, score_estimate)),
            4,
        ),
        "riskAccuracy": round(risk_accuracy(risks, prediction), 4),
        "confusionMatrix": risk_confusion(risks, prediction),
    }


def main() -> None:
    args = parse_args()
    rows = [
        row
        for row in read_rows(Path(args.dataset))
        if row.get("pose_status") == "ok"
    ]
    if len(rows) < 10:
        raise SystemExit("Not enough labeled pose rows to train XGBoost.")

    x = np.array(
        [[numeric(row, column) for column in FEATURE_COLUMNS] for row in rows],
        dtype=np.float32,
    )
    y = np.array([target_probability(row) for row in rows], dtype=np.float32)
    risks = [
        row.get("training_risk_level")
        or probability_to_risk(target_probability(row))
        for row in rows
    ]
    groups = np.array(
        [
            f"{row.get('activity', '')}:{row.get('session_id', '')}:{row.get('source_path', '')}"
            for row in rows
        ]
    )

    splitter = GroupShuffleSplit(
        n_splits=1,
        test_size=args.test_size,
        random_state=args.random_state,
    )
    train_index, test_index = next(splitter.split(x, y, groups))

    model = XGBRegressor(
        objective="reg:squarederror",
        n_estimators=96,
        max_depth=3,
        learning_rate=0.055,
        subsample=0.88,
        colsample_bytree=0.86,
        reg_lambda=1.4,
        reg_alpha=0.02,
        min_child_weight=2,
        random_state=args.random_state,
        n_jobs=1,
        tree_method="hist",
    )
    model.fit(x[train_index], y[train_index])

    metrics = {
        "trainedAt": date.today().isoformat(),
        "version": f"reba-iso-xgboost-onnx-{date.today().isoformat()}",
        "modelSource": "research_team_reba2_iso11228_document_guided_xgboost",
        "featureSchemaId": FEATURE_SCHEMA_ID,
        "inputFeatureCount": len(FEATURE_COLUMNS),
        "featureCount": len(FEATURE_COLUMNS),
        "featureEngineering": "raw_movenet_51",
        "trainingSource": str(args.dataset),
        "labelSource": (
            "Research-team REBA-2 labels combined with ISO 11228 workbook "
            "labels, optional calibration labels, and document-guided "
            "ISO 11228-3 pseudo-label fallback"
        ),
        "trainingSampleCount": int(len(train_index)),
        "holdoutSampleCount": int(len(test_index)),
        "totalSampleCount": int(len(rows)),
        "riskDistribution": dict(Counter(risks)),
        "labelSourceDistribution": dict(
            Counter(row.get("training_label_source", "") for row in rows)
        ),
        "labelMatchDistribution": dict(
            Counter(row.get("training_label_match_type", "") for row in rows)
        ),
        "calibrationMatchedSampleCount": sum(
            1
            for row in rows
            if row.get("training_label_match_type", "").startswith("calibration_")
        ),
        "thresholds": THRESHOLDS,
        "train": evaluate(
            model=model,
            x=x[train_index],
            y=y[train_index],
            risks=[risks[i] for i in train_index],
        ),
        "holdout": evaluate(
            model=model,
            x=x[test_index],
            y=y[test_index],
            risks=[risks[i] for i in test_index],
        ),
        "featureNames": FEATURE_COLUMNS,
        "notes": [
            "The ONNX model accepts the app's canonical 51 raw MoveNet features.",
            "The target is the app's combined REBA + ISO REBA-equivalent risk probability.",
            "The assessment model does not use the legacy MoveNet Logistic Regression artifact.",
            "Calibration labels affect training only when raw pose rows for the calibrated activity/session are present.",
            "Economic impact data is intentionally excluded from training and remains a post-assessment impact layer.",
        ],
    }

    output = Path(args.output)
    output.parent.mkdir(parents=True, exist_ok=True)
    onnx_model = convert_xgboost(
        model,
        initial_types=[("input", FloatTensorType([None, len(FEATURE_COLUMNS)]))],
        target_opset=15,
    )
    output.write_bytes(onnx_model.SerializeToString())

    metadata_output = Path(args.metadata_output)
    metadata_output.parent.mkdir(parents=True, exist_ok=True)
    metadata_output.write_text(
        json.dumps(metrics, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    metrics_output = Path(args.metrics_output)
    metrics_output.parent.mkdir(parents=True, exist_ok=True)
    metrics_output.write_text(
        json.dumps(metrics, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(json.dumps(metrics, ensure_ascii=False, indent=2))


if __name__ == "__main__":
    main()
